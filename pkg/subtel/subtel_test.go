// TestPacket_Serialize verifies Packet_Serialize behavior.
func TestPacket_Serialize(t *testing.T) {
	t.Run("serializes packet to byte array", func(t *testing.T) {
		destinationID, _ := deviceid.FromByteArray([]byte{0xaa, 0xbb, 0xcc, 0xdd})
		senderID, _ := deviceid.FromByteArray([]byte{0x11, 0x22, 0x33, 0x44})
		userData := []byte{0x01, 0x02, 0x03, 0x04}

		packet := Packet{
			DestinationID: destinationID,
			Rorg:          enums.RorgVLD,
			Rssi:          0xee,
			SecurityLevel: 0x03,
			Status:        0x00,
			SubTelNum:     0x01,
			SenderID:      senderID,
			SubTels:       []SubTel{{Tick: 0x01, Rssi: 0x02, Status: 0x03}},
			Timestamp:     0x1234,
			UserData:      userData,
		}

		serialized := packet.Serialize()

		// Should not be empty
		if len(serialized) == 0 {
			t.Errorf("expected non-empty serialized data")
		}

		// Should start with sync byte 0x55
		if serialized[0] != 0x55 {
			t.Errorf("expected sync byte 0x55, got 0x%02x", serialized[0])
		}

		// Should contain the expected data
		if !reflect.DeepEqual(serialized, packet.ToEsp3().Serialize()) {
			t.Errorf("serialized data should match ToEsp3().Serialize()")
		}
	})
}

